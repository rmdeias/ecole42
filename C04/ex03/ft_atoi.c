int	ft_atoi(char *str)
{
	int m;
	int i;
	int nb;

	i = 0;
	m = 1;
	nb = 0;
	while (str[i] != '\0' && (str[i] == ' ' || str[i] == '\n' || str[i] == '\f'
			|| str[i] == '\v' || str[i] == '\t' || str[i] == '\r'))
	{
		i++;
	}
	while (str[i] != '\0' && (str[i] == '+' || str[i] == '-'))
	{
		if (str[i] == '-')
			m *= -1;
		i++;
	}
	while (str[i] != '\0' && str[i] <= '9' && str[i] >= '0')
	{
		nb = nb * 10 + (str[i] - 48);
		i++;
	}
	return (nb * m);
}
