int	ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int i;

	i = 0;
	while (i + 2 < length)
	{
		if ((*f)(tab[i], tab[i + 1]) < 0)
			if ((*f)(tab[i + 1], tab[i + 2]) > 0)
				return (0);
		if ((*f)(tab[i], tab[i + 1]) > 0)
			if ((*f)(tab[i + 1], tab[i + 2]) < 0)
				return (0);
		i++;
	}
	return (1);
}
