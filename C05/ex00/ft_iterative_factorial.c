int	ft_iterative_factorial(int nb)
{
	int i;

	if (nb < 0)
		return (0);
	else if (nb <= 1)
		return (1);
	else
	{
		i = nb - 1;
		while (i > 0)
		{
			nb = nb * i;
			i--;
		}
	}
	return (nb);
}
